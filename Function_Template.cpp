/*
A function template is a function which contains generic code to operate on different types of data. 
This enables a programmer to write functions without having to specify the exact type of parameters.
Syntax for defining a template function is as follows:
      template<class Type, ...>
      return–type function–name(Type arg1, ...)
      {
      //Body of function template
      }
keyword template followed by a list of template type arguments or also called generic arguments.
The template keyword tells the compiler that what follows is a template. Here, class is a keyword and Type is the name of generic argument.
*/
